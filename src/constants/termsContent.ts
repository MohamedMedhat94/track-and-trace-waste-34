export const getTermsForCompanyType = (companyType: 'generator' | 'transporter' | 'recycler'): string => {
  let specificTerms = '';
  
  switch (companyType) {
    case 'generator':
      specificTerms = GENERATOR_TERMS;
      break;
    case 'transporter':
      specificTerms = TRANSPORTER_TERMS;
      break;
    case 'recycler':
      specificTerms = RECYCLER_TERMS;
      break;
  }
  
  return `${COMMON_TERMS}\n\n${specificTerms}`;
};
